def reachable(spec):
    def counts(n):
        d={0:1}
        for _ in range(n):
            nd={}
            for s,c in d.items():
                for f in range(1,6): nd[s+f]=nd.get(s+f,0)+c
            d=nd
        return d
    sN,tN=spec["axes"]["self"]["items"],spec["axes"]["team"]["items"]
    thr=spec["scoring"]["quadrant"]["threshold"]; B=spec["scoring"]["bands"]; out=set()
    for ss in counts(sN):
        for ts in counts(tN):
            s,t=ss/sN,ts/tN; sH,tH=s>=thr,t>=thr
            q='aligned' if(sH and tH)else 'lone' if(sH and not tH)else 'martyr' if(not sH and tH)else 'grinder'
            m=(s+t)/2; g=abs(s-t); c=B[q]
            band=(c["high"] if m>=c["cut"] else c["low"]) if c["measure"]=="mean" else (c["high"] if g>=c["cut"] else c["low"])
            out.add(f"{q}|{band}")
    return out

def compile_landings(spec):
    """Compose every reachable address into a landing. text includes ALL opener
    variants, so the voice/evidence lint covers every phrasing a user could see."""
    qd=spec["content"]["quadrants"]; dl=spec["content"]["depthLeads"]; land={}
    for addr in reachable(spec):
        q=addr.split("|")[0]; c=qd[q]
        text=" ".join(c["opener"]) + " " + dl[addr] + " " + " ".join(c["body"])
        land[addr]={"gap_family":c["gap_family"],"validate_first":c["validate_first"],
                    "licensed_inputs":["self","team"],"text":text}
    return land

