def main():
    args=[a for a in sys.argv[1:] if a!="--check"]
    check_only="--check" in sys.argv
    spec=json.load(open(args[0]))
    land=compile_landings(spec)
    aux=list((spec.get("content",{}).get("normative",{}) or {}).values())
    check_spec={**spec,"landings":land,"aux_texts":aux}
    R=gate_check.run(check_spec)
    blocked=[g for g,sev,st,_ in R if sev=="BLOCK" and st=="FAIL"]
    print("ENGINE gate pre-check:", "REFUSED "+str(blocked) if blocked else "OK")
    for g,sev,st,d in R:
        if sev=="BLOCK": print(f"   [{st}] {g}")
    if blocked:
        print("Emission aborted."); sys.exit(1)
    if check_only:
        print("Check-only: all blocking gates pass (no artifact written)."); sys.exit(0)
    open(args[1],"w").write(emit_html(spec))
    print("Emitted:", args[1])

if __name__=="__main__": main()
